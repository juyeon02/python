# # 5. 편의점 도시락 구매하기
# # ===========================

price_kimbab = 2500
price_samgak = 1500
price_dosirak = 4000

money = int(input('금액을 입력해주세요:'))

food = input('구매할 식품을 입력하세요: (김밥, 삼각김밥, 도시락)')

if food == '김밥':
    if money >= price_kimbab:
        print('구매 성공')
    else:
        print('구매 실패')
elif food == '삼각김밥':
    if money >= price_samgak:
        print('구매 성공')
    else:
        print('구매 실패')
elif food == '도시락':
    if money >= price_dosirak:
        print('구매 성공')
    else:
        print('구매 실패')
else:
    print('잘못된 메뉴 입니다.')
