# ===========================
# for문 - 정해진 횟수만큼 반복
# ===========================

# print('안녕하세요')  # 단순 출력

# range(5) → 0부터 4까지 총 5번 반복
for i in range(5):
    print('안녕하세요')  # 5번 반복해서 출력


# ===========================
# range 함수의 기본 구조
# ===========================

# range(끝) → 0부터 끝-1까지
range(5)  # 0, 1, 2, 3, 4

# range(시작, 끝) → 시작부터 끝-1까지
range(2, 6)  # 2, 3, 4, 5

# range(시작, 끝, 간격) → 간격만큼 증가하면서 반복
range(2, 6, 2)  # 2, 4


