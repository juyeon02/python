# ===========================
# 구구단 만들기 (4단만 출력)
# ===========================

num = 4
print('4단')
for i in range(1, 10):  # 1부터 9까지 반복
    print(f'{num} * {i} = {num * i}')  # 곱셈 결과 출력
print('==========')
print()


# ===========================
# 구구단 전체 (1단 ~ 9단)
# ===========================
for num in range(1, 10):  # 1단부터 9단까지 반복
    print(f"=== {num} 단 === ")

    for i in range(1, 10):  # 각 단에서 1~9까지 곱하기
        print(f'{num} * {i} = {num * i}')
    print()  # 단 사이에 줄바꿈


# ===========================
# 리스트와 for문
# ===========================

# 과일 리스트 선언
fruits = ["사과", "바나나", "오렌지", "포도"]

# 리스트의 요소를 하나씩 꺼내어 출력
for fruit in fruits:
    print(f"과일 : {fruit}")


# 점수 리스트 선언
scores = [65, 27, 87, 86, 65, 27, 87,
          86, 65, 27, 87, 86, 65, 27,
          87, 86]

# 총점과 개수를 저장할 변수 초기화
total = 0
count = 0

# 점수를 하나씩 꺼내면서 총점과 개수 누적
for score in scores:
    total += score
    count += 1
    print(f"점수 : {score}")

# 최종 결과 출력
print("총점: ", total)
print("평균: ", total / count)
print()


# ===========================
# 문자열 순회 (문자 하나씩 출력)
# ===========================

print("=========")
word = "Python"

for char in word:  # 문자열도 리스트처럼 반복 가능
    print(char, end=' ')  # 줄바꿈 없이 출력 (공백으로 구분)
print()


# ===========================
# 중첩 for문 - 반복 속의 반복
# ===========================

# 별 패턴 1 : 직각삼각형
# i가 1일 때 → 별 1개
# i가 2일 때 → 별 2개
# ...
# i가 5일 때 → 별 5개
for i in range(1, 6):      # i = 1, 2, 3, 4, 5
    for j in range(i):     # j는 i번 반복
        print('*', end='')  # 별 출력 (줄바꿈 없이)
    print()                # 줄바꿈


# 별 패턴 2 : 정사각형
# 5행 5열의 별 출력
# *****
# *****
# *****
# *****
# *****
for i in range(5):          # 5번 반복 (행)
    for j in range(5):      # 5번 반복 (열)
        print('*', end='')  # 별 출력
    print()                 # 줄바꿈
